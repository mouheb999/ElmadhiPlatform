-- ============================================================
-- 1. Schema changes to `exercises` (additive + constraint relax)
-- ============================================================
ALTER TABLE exercises
  ADD COLUMN IF NOT EXISTS exercise_type TEXT NOT NULL DEFAULT 'strength'
    CHECK (exercise_type IN ('strength','cardio','stretching')),
  ADD COLUMN IF NOT EXISTS needs_tier_review BOOLEAN NOT NULL DEFAULT FALSE,
  ADD COLUMN IF NOT EXISTS needs_home_review BOOLEAN NOT NULL DEFAULT FALSE,
  ADD COLUMN IF NOT EXISTS ar_needs_review   BOOLEAN NOT NULL DEFAULT FALSE,
  ADD COLUMN IF NOT EXISTS slug TEXT;

-- primary_muscle is NULL for cardio/stretching rows.
ALTER TABLE exercises ALTER COLUMN primary_muscle DROP NOT NULL;

-- 8th equipment value: bare weight plate (Svend Press, Plate Pinch).
ALTER TABLE exercises DROP CONSTRAINT IF EXISTS exercises_equipment_check;
ALTER TABLE exercises ADD CONSTRAINT exercises_equipment_check
  CHECK (equipment IN ('barbell','dumbbell','cable','machine','bodyweight','kettlebell','band','plate'));

-- Guard: a strength row must have a muscle; cardio/stretching must not.
ALTER TABLE exercises DROP CONSTRAINT IF EXISTS exercises_muscle_by_type_check;
ALTER TABLE exercises ADD CONSTRAINT exercises_muscle_by_type_check CHECK (
  (exercise_type = 'strength' AND primary_muscle IS NOT NULL)
  OR (exercise_type <> 'strength' AND primary_muscle IS NULL)
);

-- slug is the join key to public/exercise-library (slugify(name_en)).
CREATE UNIQUE INDEX IF NOT EXISTS idx_exercises_slug ON exercises(slug);

